const double Rearth		= 6371000.;		// Earth radius
const double REFF		= 1.333 * Rearth;

/*******************************************************************************
*																			
* Function name: rainloss()                                        	
*                                                               			
* Description:   Calculation of two-way power loss in rain 
**
* Return value:  power attenuation factor (<=1)
*
*******************************************************************************/

double rainloss(double rain_rate, double rain_h, double rain_rmin, double rain_rmax, double elevation, double freq, int pol)
{
	double x = REFF*sin(elevation);
	double y = sqrt(2*REFF*rain_h + rain_h*rain_h + x*x) - x;
	if (y > rain_rmax) y = rain_rmax;
	double path = y - rain_rmin;
	double z =1;
	if (path > 0) 
	{
		double dB = 0.001*path*rainattenuation_dbkm(rain_rate, freq, pol);
		z = pow((double)10.0, -dB/10);
	}
	return z;
}
