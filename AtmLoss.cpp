//********************************************************************************
//																			
//  Function name: atmloss()                                        	
//                                                               			
//  Description:   Calculation of two-way atmosphere loss by look up table method
//                                                               			
//  Arguments:                                                 					
//		range 		- Range distance of target (m) 
//		elevation 	- Beam elevation coordinate (radian) 
//		frequency	- carrier frequency ( Hz )
//
//  Return value: attenuation factor in power units ( < 1 )
//                                                              	
//  Note. The results are true for frequencies from 0.1 to 10 GHz: 
//        if freq. > 10  Ghz then loss = loss(10  GHz) 
//        if freq. < 0.1 Ghz then loss = loss(0.1 GHz) 
// ********************************************************************************


double atmloss(double elevation, double range, double frequency) 
{
double Rkm  = range/1000;		/* km */  
double Edeg = elevation*180/PI; /* deg */  
double Fghz = frequency*1e-9;	/* GHz */

int e, f, r;
int e1,f1,r1;

double l0,l1,l;
double h0,h1,h;
double a,b,c,loss;

// limitation
if( Rkm <R[0]) return 1.;
if( Edeg<E[0]) Edeg=E[0];
if( Fghz<F[0]) Fghz=F[0];

if( Rkm >R[NR-1]) Rkm=R[NR-1];
if( Edeg>E[NE-1]) Edeg=E[NE-1];
if( Fghz>F[NF-1]) Fghz=F[NF-1];

// find the point for look up table method
r=0; while(Rkm >= R[r]) r++; r -=1;
f=0; while(Fghz>= F[f]) f++; f -=1;
e=0; while(Edeg>= E[e]) e++; e -=1;

// wheighting coefficients for linear interpolation
if(r<NR-1) {a = (Rkm-R[r])/(R[r+1]-R[r]); r1=r+1;} else {a=0.; r1=r;}
if(f<NF-1) {b = log(Fghz/F[f])/log(F[f+1]/F[f]); f1=f+1;} else {b=0.; f1=f;}
if(e<NE-1) {if(Edeg<=2) c=(Edeg-E[e])/(E[e+1]-E[e]); else c=log(Edeg/E[e])/log(E[e+1]/E[e]); e1=e+1;} else {c=0.;e1=e;}

// Loss calculation for the low elevation E[e]
// interpolation along range for low and high frequencies
l0= LOSS[e][r][f]  + a*(LOSS[e][r1][f]  -LOSS[e][r][f]);
l1= LOSS[e][r][f1] + a*(LOSS[e][r1][f1] -LOSS[e][r][f1]);
// interpolation along frequency	
l = l0 + b*(l1-l0);

// Loss calculation for the high elevation E[e+1]
// interpolation along range for low and high frequencies
h0= LOSS[e1][r][f] + a*(LOSS[e1][r1][f] -LOSS[e1][r][f]);
h1= LOSS[e1][r][f1]+ a*(LOSS[e1][r1][f1]-LOSS[e1][r][f1]);
// interpolation along frequency	
h = h0 + b*(h1-h0);

// Final interpolation along elevation	
loss = l + c*(h-l); // dB

loss = pow((double)10.0,-loss/10.); // power attenuation factor 
return loss;
}
